package NYP_paket_odev2;

public class Kurs {
	
	
	private int KursId;
	private String KursAd;
	
	public Kurs(int kursId, String kursAd) {
		super();
		KursId = kursId;
		KursAd = kursAd;
	}
	
	//---------------------------------------------------------------
	
	
	//------------------------------------------------------------
	public int getKursId() {
		return KursId;
	}
	public void setKursId(int kursId) {
		KursId = kursId;
	}
	public String getKursAd() {
		return KursAd;
	}
	public void setKursAd(String kursAd) {
		KursAd = kursAd;
	}
	
	

	
}
